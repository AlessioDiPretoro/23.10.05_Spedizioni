        public override string[] GetRolesForUser(string username)
        {
            string conn = ConfigurationManager.ConnectionStrings["ConString"].ConnectionString;
            SqlConnection cmd = new SqlConnection(conn);
            List<string> roles = new List<string>();
            try
            {
                cmd.Open();
                SqlCommand sqlCommand = new SqlCommand("Select Role FROM Users WHERE Username=@Username", cmd);
                sqlCommand.Parameters.AddWithValue("Username", username);
                SqlDataReader reader = sqlCommand.ExecuteReader();
                while (reader.Read())
                {
                    string ruolo = reader["Role"].ToString();
                    roles.Add(ruolo);
                }
            }
            catch
            {
            }
            finally
            {
                cmd.Close();
            }
            return roles.ToArray();
        }

